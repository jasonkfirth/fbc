/*
    FreeBASIC Sound Library (sfxlib)
    --------------------------------

    File: sfx_buffer.c

    Purpose:

        Implement the intermediate audio buffer used by the sound
        subsystem.

        The buffer stores the mixed audio output generated by the
        software mixer before it is consumed by platform drivers.

    Responsibilities:

        • buffer allocation
        • buffer clearing
        • buffer read/write access
        • buffer size management

    This file intentionally does NOT contain:

        • audio synthesis
        • mixer algorithms
        • driver playback logic
        • command parsing

    Architectural overview:

        synthesis → mixer → intermediate buffer → driver → OS audio
*/

#include <stdlib.h>
#include <string.h>

#include "fb_sfx.h"
#include "fb_sfx_internal.h"
#include "fb_sfx_buffer.h"


/* ------------------------------------------------------------------------- */
/* Buffer initialization                                                     */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferInit()

    Allocate the main intermediate audio buffer used by the mixer.
*/

int fb_sfxBufferInit(int frames)
{
    int samples;
    size_t size;

    if (!__fb_sfx)
        return -1;

    if (frames <= 0)
        return -1;

    samples = frames * __fb_sfx->output_channels;
    size = samples * sizeof(float);

    __fb_sfx->mixbuffer = (float*)malloc(size);

    if (!__fb_sfx->mixbuffer)
        return -1;

    memset(__fb_sfx->mixbuffer, 0, size);

    __fb_sfx->buffer_frames = frames;

    SFX_DEBUG("sfx_buffer: allocated %d frames (%zu bytes)",
              frames, size);

    return 0;
}


/* ------------------------------------------------------------------------- */
/* Buffer shutdown                                                           */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferShutdown()

    Release the intermediate audio buffer.
*/

void fb_sfxBufferShutdown(void)
{
    if (!__fb_sfx)
        return;

    if (__fb_sfx->mixbuffer)
    {
        free(__fb_sfx->mixbuffer);
        __fb_sfx->mixbuffer = NULL;
    }

    __fb_sfx->buffer_frames = 0;

    SFX_DEBUG("sfx_buffer: buffer released");
}


/* ------------------------------------------------------------------------- */
/* Buffer clear                                                              */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferClear()

    Reset the buffer to silence.
*/

void fb_sfxBufferClear(void)
{
    size_t size;

    if (!__fb_sfx)
        return;

    if (!__fb_sfx->mixbuffer)
        return;

    size = __fb_sfx->buffer_frames *
           __fb_sfx->output_channels *
           sizeof(float);

    memset(__fb_sfx->mixbuffer, 0, size);
}


/* ------------------------------------------------------------------------- */
/* Buffer access                                                             */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferPtr()

    Return pointer to the intermediate mix buffer.

    Drivers use this pointer to retrieve generated audio.
*/

float *fb_sfxBufferPtr(void)
{
    if (!__fb_sfx)
        return NULL;

    return __fb_sfx->mixbuffer;
}


/*
    fb_sfxBufferFrames()

    Return the number of frames contained in the buffer.
*/

int fb_sfxBufferFrames(void)
{
    if (!__fb_sfx)
        return 0;

    return __fb_sfx->buffer_frames;
}


/*
    fb_sfxBufferSamples()

    Return the number of samples in the buffer.
*/

int fb_sfxBufferSamples(void)
{
    if (!__fb_sfx)
        return 0;

    return __fb_sfx->buffer_frames * __fb_sfx->output_channels;
}


/* ------------------------------------------------------------------------- */
/* Buffer write helper                                                       */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferWrite()

    Write a sample to the buffer.

    This helper is primarily intended for testing or for
    alternative synthesis paths.
*/

void fb_sfxBufferWrite(int index, float value)
{
    int samples;

    if (!__fb_sfx)
        return;

    if (!__fb_sfx->mixbuffer)
        return;

    samples = __fb_sfx->buffer_frames * __fb_sfx->output_channels;

    if (index < 0 || index >= samples)
        return;

    __fb_sfx->mixbuffer[index] = value;
}


/* ------------------------------------------------------------------------- */
/* Buffer read helper                                                        */
/* ------------------------------------------------------------------------- */

/*
    fb_sfxBufferRead()

    Read a sample from the intermediate buffer.

    This function is useful for automated testing of the
    synthesis pipeline.
*/

float fb_sfxBufferRead(int index)
{
    int samples;

    if (!__fb_sfx)
        return 0.0f;

    if (!__fb_sfx->mixbuffer)
        return 0.0f;

    samples = __fb_sfx->buffer_frames * __fb_sfx->output_channels;

    if (index < 0 || index >= samples)
        return 0.0f;

    return __fb_sfx->mixbuffer[index];
}


void fb_sfxMixBufferInit(void)
{
    if (!__fb_sfx)
        return;

    (void)fb_sfxBufferInit(__fb_sfx->buffer_size);
}


void fb_sfxMixBufferShutdown(void)
{
    fb_sfxBufferShutdown();
}


int fb_sfxMixBufferWrite(const float *samples, int frames)
{
    int total;
    int i;

    if (!samples || frames <= 0)
        return 0;

    total = frames * (__fb_sfx ? __fb_sfx->output_channels : FB_SFX_DEFAULT_CHANNELS);

    for (i = 0; i < total; ++i)
        fb_sfxBufferWrite(i, samples[i]);

    return frames;
}


int fb_sfxMixBufferRead(float *samples, int frames)
{
    int total;
    int i;

    if (!samples || frames <= 0)
        return 0;

    total = frames * (__fb_sfx ? __fb_sfx->output_channels : FB_SFX_DEFAULT_CHANNELS);

    for (i = 0; i < total; ++i)
        samples[i] = fb_sfxBufferRead(i);

    return frames;
}


